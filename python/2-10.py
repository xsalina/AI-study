# ### **Day 10 — Few-shot 示例设计**
# 内容：案例选择技巧
# 练习：构建“电商客服 AI”几-shot 示例

# 我们要设计用户输入 → AI 输出的几个案例，让模型学习如何回答电商客服相关问题。

# 通过这些示例，模型可以明白：

# 问题类型（订单、物流、退换货等）

# 回答风格（礼貌、简洁、专业）

# 输出格式（可以是 JSON、文本等）

# few-shot 示例的核心目的是让模型对电商客服任务形成正确的行为模式，而不是一次性告诉它所有规则。


