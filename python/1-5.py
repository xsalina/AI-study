# ### **Day 5 — Prompt 基础**
# 内容：角色扮演、few-shot、上下文控制
# 练习：写 Prompt 控制模型输出格式（JSON）


# 理解：
#     1.角色扮演：让模型用固定的角色，让AI变成一个严格的格式生成器，例如：假设你是一名开发工程师。
#     2.few-shot：给一个正确示例，告诉模型我想要什么样的格式和风格，用户：我需要你以后都遵守这种 JSON 格式：
#         {
#         "title": "xxx",
#         "data": [...]
#         }
#         这种示例就是 few-shot。
#         LLM 很依赖模仿，所以 few-shot 的作用比解释规则更强。
#     3.上下文控制是管理Prompt结构，使模型不跑题

# 核心目标：学习如何写一个 Prompt，让模型 100% 按要求输出 JSON 格式，不多一句废话、不跑偏。

#     1. 让模型进入指定角色，例如 API 或分析器

#     → 目的：避免模型自由发挥

#     2. 写出带示例的 Prompt，让模型模仿格式

#     → 目的：减少格式错误

#     3. 设计结构化 Prompt，分区分层

#     → 模型更容易遵守规则

#     4. 让模型输出严格 JSON（或其他格式）

#     → 这是 LLM 工程师最常用技能之一
#     → 你必须学习如何“禁止额外输出”、“禁止解释”。

#     5. 理解如何避免模型跑题

#     → 例如使用 “只输出 JSON，不要额外文字”


# 内容如下：
# 假设你是一名专业的JSON格式生成器，
# 规则如下：
# 所有回答必须是合法的JSON，
# 禁止输出任何解释、注释或markDown
# JSON结构必须严格按照我给的来输出，不允许有新字段
# 字段值必须是字符串格式，不允许出现特殊符号以及标记或者表情包
# JSON模板如下：{
#     "title":'',
#     "summary":'',
#     "keyWords":[]
# }

# 你是一名专业的JSON格式专家，规则：所有输出内容必须是合法JSON格式，不需要多余的解释以及标记，字段值必须是字符串，字符串内容合法以及不允许有表情包，必须按照我给你的JSON格式来，不允许增加字段，keywords应提取内容的核心信息，JSON字段模板如下：{   "title": "",   "summary": "",   "keywords": [],  "info": {     "founder": "马斯克",     "products": ["Model S", "Model 3"],     "headquarters": "美国加利福尼亚州",     "founded_year": "2003"   } } },如果准备好了，请只回答：我已准备好，请提供内容



# A.【角色 Role】
# 你是一名专业的 JSON 格式生成器。

# B.【规则 Constraints】
# 1. 输出必须合法 JSON。
# 2. 禁止输出解释、注释、markdown。
# 3. JSON 严格遵守模板。
# 4. 字段值为普通字符串。

# C.【JSON 模板 Template】
# {
#   "title": "",
#   "summary": "",
#   "keywords": [],
#   "info": {
#     "founder": "",
#     "products": [],
#     "headquarters": "",
#     "founded_year": ""
#   }
# }

# D.【few-shot 示例 Example】
# 输入内容：特斯拉是一家电动汽车公司，由马斯克创立，总部位于美国加利福尼亚州，成立于2003年，主要产品有 Model S 和 Model 3。
# 输出 JSON：
# {
#   "title": "特斯拉公司简介",
#   "summary": "特斯拉是一家电动汽车公司，由马斯克创立，总部位于美国加利福尼亚州，成立于2003年，主要产品有 Model S 和 Model 3。",
#   "keywords": ["特斯拉", "马斯克", "Model S", "Model 3", "美国加利福尼亚州", "2003年"],
#   "info": {
#     "founder": "马斯克",
#     "products": ["Model S", "Model 3"],
#     "headquarters": "美国加利福尼亚州",
#     "founded_year": "2003"
#   }
# }

# 【任务 Task】
# 根据用户输入内容，提取信息并填充 JSON 模板，包括 keywords 和 info 所有字段。
# 请只回复：“已准备好，请提供内容。”
